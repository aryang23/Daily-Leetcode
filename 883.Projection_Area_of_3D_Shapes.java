class Solution {
    public int projectionArea(int[][] grid) {
        int n = grid.length;
        int ans = 0;
        
        for(int i=0;i<n;i++)
        {
            int bestRow = 0;  // largest of grid[i][j]
            int bestCol = 0;  // largest of grid[j][i]
            for(int j=0;j<n;j++)
            {
                if (grid[i][j] > 0) ans++;  // top shadow
                bestRow = Math.max(bestRow, grid[i][j]);
                bestCol = Math.max(bestCol, grid[j][i]);
            }
            ans += bestRow + bestCol;
        }
        return ans;
    }
}
